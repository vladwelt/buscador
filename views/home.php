<h4>Bienvenido</h4>
<br>
<p>La Recuperación de la Información, tiene por objeto: identificar, localizar, seleccionar y acceder a los recursos de información útiles al usuario.</p>
<br>
<p>Los usuarios podrán realizar búsquedas de textos con las extensiones .txt, .html en los documentos que seran cargados en el sistema por un administrador.
La búsqueda se puede realizar utilizando los siguientes modelos:</p>
<br>
